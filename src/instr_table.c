const instr_entry_t *instr_lookup(const char *name)
{
    if (!name)
        return NULL;
    for (size_t i = 0; i < sizeof(instr_table) / sizeof(instr_table[0]); ++i)
    {
        if (strcmp(instr_table[i].name, name) == 0)
            return &instr_table[i];
    }
    return NULL;
}

size_t instr_table_count(void)
{
    return sizeof(instr_table) / sizeof(instr_table[0]);
}
